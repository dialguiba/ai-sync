const generatedHeader = "<!-- Generated by ai-sync. Do not edit directly; source is .ai/. -->\n\n"

var allTargets = []string{"claude", "codex", "kiro"}

// BuildInfo describes the binary version metadata shown by the version command.
type BuildInfo struct {
	Version string
	Commit  string
	Date    string
}

// Run executes ai-sync in workdir with CLI-style args and returns user-facing output.
func Run(workdir string, args []string) (string, error) {
	return RunWithBuildInfo(workdir, args, BuildInfo{Version: "dev", Commit: "unknown", Date: "unknown"})
}

// RunWithBuildInfo executes ai-sync with explicit build metadata.
func RunWithBuildInfo(workdir string, args []string, build BuildInfo) (string, error) {
	if len(args) > 0 && args[0] == "init" {
		return initSource(workdir)
	}
	if len(args) > 0 && args[0] == "convention" {
		return conventionText(), nil
	}
	if len(args) > 0 && args[0] == "list" {
		return listGeneratedFiles(workdir, args[1:])
	}
	if wantsVersion(args) {
		return versionText(build), nil
	}
	if wantsHelp(args) {
		return helpText(), nil
	}

	var opts options
	flags := flag.NewFlagSet("ai-sync", flag.ContinueOnError)
	var flagOutput bytes.Buffer
	flags.SetOutput(&flagOutput)
	flags.StringVar(&opts.target, "target", "", "target to generate: claude, codex, or kiro")
	flags.BoolVar(&opts.dryRun, "dry-run", false, "show changes without writing")
	if err := flags.Parse(args); err != nil {
		output := flagOutput.String()
		if errors.Is(err, flag.ErrHelp) {
			return output, nil
		}
		return strings.TrimSpace(output), err
	}
	if flags.NArg() > 0 {
		return "", fmt.Errorf("unknown command %q", flags.Arg(0))
	}

	outputs, err := renderOutputs(workdir, opts.target)
	if err != nil {
		return "", err
	}

	targets, err := selectedTargets(opts.target)
	if err != nil {
		return "", err
	}

	changes, err := pruneTargetOutputs(workdir, targets, outputs, opts.dryRun)
	if err != nil {
		return "", err
	}
	written, err := applyOutputs(workdir, outputs, opts.dryRun)
	if err != nil {
		return "", err
	}
	changes = append(changes, written...)
	if len(changes) == 0 {
		return "no changes\n", nil
	}
	return strings.Join(changes, "\n") + "\n", nil
}

func listGeneratedFiles(workdir string, args []string) (string, error) {
	var opts options
	flags := flag.NewFlagSet("ai-sync list", flag.ContinueOnError)
	var flagOutput bytes.Buffer
	flags.SetOutput(&flagOutput)
	flags.StringVar(&opts.target, "target", "", "target to list: claude, codex, or kiro")
	if err := flags.Parse(args); err != nil {
		output := flagOutput.String()
		if errors.Is(err, flag.ErrHelp) {
			return output, nil
		}
		return strings.TrimSpace(output), err
	}
	if flags.NArg() > 0 {
		return "", fmt.Errorf("unknown list argument %q", flags.Arg(0))
	}

	outputs, err := renderOutputs(workdir, opts.target)
	if err != nil {
		return "", err
	}
	paths := make([]string, 0, len(outputs))
	for _, output := range outputs {
		paths = append(paths, filepath.ToSlash(output.Path))
	}
	sort.Strings(paths)
	return strings.Join(paths, "\n") + "\n", nil
}

func wantsVersion(args []string) bool {
	for _, arg := range args {
		if arg == "version" || arg == "--version" || arg == "-v" {
			return true
		}
	}
	return false
}

func wantsHelp(args []string) bool {
	for _, arg := range args {
		if arg == "--help" || arg == "-h" {
			return true
		}
	}
	return false
}

